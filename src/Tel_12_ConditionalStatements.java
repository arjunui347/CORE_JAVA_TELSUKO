public class Tel_12_ConditionalStatements {

    public static void main(String[] args) {

        int x = 28;
        if(x>10 && x<=20){
            System.out.println("Hello");
        }else {
            System.out.println("Bye");
        }

        int x1 = 6;
        int y1 = 9;

        if(x1>y1){
            System.out.println(x1);
        }else{
            System.out.println(y1);
        }


    }
}